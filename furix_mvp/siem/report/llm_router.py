def _count_by_prefix(entities: List[dict]) -> dict:
    """Count entities by their placeholder category prefix."""
    counts: Dict[str, int] = {}
    for ent in entities:
        key = ent.get("entity_key") or ent.get("entity", "")
        match = re.match(r'^([A-Z]+(?:_[A-Z]+)*)_\d+$', key)
        if match:
            category = match.group(1)
        else:
            category = (ent.get("entity_type") or ent.get("type", "UNKNOWN")).upper()
        counts[category] = counts.get(category, 0) + 1
    return counts


def _format_stage(stage: dict) -> str:
    """Compact one-stage block for the high-level timeline section."""
    s    = stage.get("stage", "?")
    name = stage.get("name", "Unknown")
    ts   = stage.get("first_seen", "")[:16] if stage.get("first_seen") else "?"
    ents = [e.get("entity", e.get("entity_key", "?"))
            for e in stage.get("entities", [])]
    ev   = stage.get("evidence", [])

    ent_str = ", ".join(ents[:4]) + (" (+more)" if len(ents) > 4 else "")
    ev_str  = ", ".join(ev[:4])  + (" (+more)" if len(ev)  > 4 else "")

    lines = [f"  Stage {s:>2} — {name:<22} [{ts}]"]
    if ent_str:
        lines.append(f"    Entities  : {ent_str}")
    if ev_str:
        lines.append(f"    Evidence  : {ev_str}")
    return "\n".join(lines)


def _format_rule_evidence_grouped(
    rule_hit_events:   List[dict],
    placeholder_map:   Dict[str, str],
    limit_per_rule:    int = 6,
) -> str:
    """
    Format ALL signature_rules hits, grouped by rule name.

    For each rule:
      - 1 header line with rule, count, MITRE tactic context, description
      - up to limit_per_rule instances with time + entity + IPs

    Entity / IP values are scrubbed to placeholders for the prompt
    (since the LLM never sees raw identifiers).
    """
    def _scrub(value: str) -> str:
        return placeholder_map.get(value, value) if value else ""

    by_rule: Dict[str, List[dict]] = defaultdict(list)
    for ev in rule_hit_events:
        for rule in ev.get("all_rules_fired", []):
            by_rule[rule].append(ev)

    if not by_rule:
        return "  (no signature rules fired)"

    # Order rules by kill chain stage, then by count
    def _rule_sort_key(rule_name: str):
        override = RULE_TACTIC_OVERRIDE.get(rule_name)
        stage    = override[2] if override else 99
        return (stage, -len(by_rule[rule_name]))

    blocks = []
    for rule in sorted(by_rule, key=_rule_sort_key):
        events     = by_rule[rule]
        description = RULE_DESCRIPTION.get(rule, "Anomalous behaviour")
        override   = RULE_TACTIC_OVERRIDE.get(rule)
        if override:
            tid, tactic, stage = override
            mitre_label = f"{tid} — {tactic} (Stage {stage})"
        else:
            ev0 = events[0]
            mitre_label = (f"{ev0.get('mitre_technique_id','?')} — "
                           f"{ev0.get('mitre_tactic','?')} "
                           f"(Stage {ev0.get('kill_chain_stage','?')})")

        # Confidence band — use the max across instances
        max_conf = max(float(e.get("rule_confidence", 0)) for e in events)

        header = (
            f"\n  RULE: {rule}  ({len(events)} event{'s' if len(events) != 1 else ''})  "
            f"confidence={max_conf:.2f}\n"
            f"    MITRE        : {mitre_label}\n"
            f"    Description  : {description}"
        )

        instance_lines = []
        for ev in events[:limit_per_rule]:
            ts     = ev.get("timestamp", "")[11:19]  # HH:MM:SS
            raw_entity = ev.get("entity", "")
            # Mark explicitly when no user is associated with the event so
            # the LLM does NOT hallucinate a plausible name from context.
            # Common case: firewall/network events without authenticated user.
            entity = _scrub(raw_entity) if raw_entity else "<no-user-on-event>"
            sip    = _scrub(ev.get("source_ip", ""))
            dip    = _scrub(ev.get("destination_ip", ""))
            dport  = ev.get("destination_port", 0) or ""

            parts = [f"{ts}  entity={entity}"]
            if sip:
                parts.append(f"src={sip}")
            if dip:
                ipport = f"dst={dip}" + (f":{dport}" if dport else "")
                parts.append(ipport)
            instance_lines.append("      " + "  ".join(parts))

        if len(events) > limit_per_rule:
            instance_lines.append(f"      ... (+{len(events) - limit_per_rule} more)")

        block = header + "\n    Instances    :\n" + "\n".join(instance_lines)
        blocks.append(block)

    return "\n".join(blocks)


def _build_placeholder_map(
    narrative:        dict,
    mapping_entry:    dict,
    rule_hit_events:  Optional[List[dict]] = None,
) -> Dict[str, str]:
    """
    Build a {original_value → placeholder} map by inverting the reverse map.
    Used to scrub raw values from anomaly_events.json before they enter the prompt.

    Augments the existing mapping with any new tokens found in rule_hit_events
    (destination IPs, additional usernames, etc.) that weren't in the original
    campaign IOCs. New placeholders are added to mapping_entry's reverse map
    so re-identification still works after the LLM responds.
    """
    from ..scrub.dal_scrubber import _classify

    reverse  = mapping_entry.get("reverse", {}) if mapping_entry else {}
    mapping  = mapping_entry.get("mapping", {}) if mapping_entry else {}
    fwd_map  = {orig: ph for ph, orig in reverse.items()}

    # Find max counter for each category so new placeholders don't collide
    cat_counters: Dict[str, int] = defaultdict(int)
    for ph in reverse.keys():
        match = re.match(r'^([A-Z]+(?:_[A-Z]+)*)_(\d+)$', ph)
        if match:
            cat = match.group(1)
            n   = int(match.group(2))
            if n > cat_counters[cat]:
                cat_counters[cat] = n

    if not rule_hit_events:
        return fwd_map

    # Attacker IPs (for ATTACKER_IP classification) — reuse the narrative's set
    iocs = narrative.get("llm_context", {}).get("structured_data", {}).get("iocs", {})
    raw_iocs = narrative.get("iocs", iocs)
    attacker_ips = set(raw_iocs.get("external_ips", []))
    # Also include any external IP already classified as ATTACKER_IP in existing map
    for orig, ph in fwd_map.items():
        if ph.startswith("ATTACKER_IP_"):
            attacker_ips.add(orig)

    # Scan rule_hit_events for tokens needing scrubbing
    new_tokens: List[str] = []
    for ev in rule_hit_events:
        for field in ("entity", "source_ip", "destination_ip"):
            val = ev.get(field, "")
            if val and isinstance(val, str) and len(val) >= 2:
                if val not in fwd_map:
                    new_tokens.append(val)

    # Assign placeholders for new tokens
    seen = set()
    for token in new_tokens:
        if token in seen or token in fwd_map:
            continue
        seen.add(token)
        category = _classify(token, attacker_ips)
        cat_counters[category] += 1
        placeholder = f"{category}_{cat_counters[category]}"
        fwd_map[token] = placeholder
        # Update mapping_entry in-place so re-identification can restore later
        if mapping_entry is not None:
            mapping_entry.setdefault("reverse", {})[placeholder] = token
            mapping_entry.setdefault("mapping", {})[token]        = placeholder

    return fwd_map


