def _call_gemma(messages: List[dict]) -> Tuple[dict, dict, str]:
    """Run one campaign report through the in-house Gemma.

    Re-points the source engine's OpenRouter call onto furix's
    ``llm.complete_json``: model / endpoint / temperature come from furix config;
    ``MAX_TOKENS`` overrides furix's smaller default so a full report doesn't
    truncate. ``complete_json`` parses JSON + retries internally and honours
    ``MOCK_LLM``. Returns ``(analysis, api_stats, status)``.
    """
    system = next((m["content"] for m in messages if m.get("role") == "system"), "")
    user   = next((m["content"] for m in messages if m.get("role") == "user"),   "")

    res   = llm.complete_json(system, user, max_tokens=MAX_TOKENS, mock=_MOCK_REPORT)
    stats = {
        "prompt_tokens":     getattr(res, "prompt_tokens", 0),
        "completion_tokens": getattr(res, "completion_tokens", 0),
        "latency_ms":        getattr(res, "latency_ms", 0),
        "json_mode_used":    True,
    }
    if not res:                         # empty result → call failed after retries
        stats["error"] = getattr(res, "error", "empty_response")
        return {}, stats, "failed"
    return dict(res), stats, "success"


