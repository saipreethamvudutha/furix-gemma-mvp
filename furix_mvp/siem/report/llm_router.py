class LLMRouter:
    """
    Orchestrates the single-call LLM analysis for each scrubbed campaign,
    using the full set of rule-hit events as evidence.

    Usage:
        router = LLMRouter()
        reports = router.process_campaigns(
            scrubbed_narratives,
            anomaly_store_path="results/anomaly_events.json",
            mappings_dir="results/",
            output_dir="results/",
        )
    """

    def __init__(self):
        self._scrubber = DALScrubber()

    # ------------------------------------------------------------------ #
    # Public
    # ------------------------------------------------------------------ #

    def process_campaigns(
        self,
        scrubbed_narratives: List[dict],
        anomaly_store_path:  str = "results/anomaly_events.json",
        mappings_dir:        str = "results/",
        output_dir:          str = "results/",
        *,
        mappings:            Optional[Dict[str, dict]] = None,
        min_confidence:      Optional[float] = None,
    ) -> List[dict]:
        # PII mappings: prefer the in-memory dict the scrubber returned (the
        # decoupled IPC path). Fall back to loading from disk only when not
        # supplied — the on-disk pii_mapping_*.json files are now an audit
        # artifact, not the required scrubber→router channel.
        if mappings is None:
            mappings = self._scrubber.load_mappings(mappings_dir)
        # Confidence gate: defaults to MIN_LLM_CONFIDENCE (0.70). Callers (e.g. the
        # dashboard orchestrator) may pass a lower floor to report smaller campaigns.
        floor = MIN_LLM_CONFIDENCE if min_confidence is None else min_confidence
        anomaly_store = load_anomaly_store(anomaly_store_path) or {
            "rule_hit_events": [], "all_detections": [], "metadata": {}
        }
        rule_hit_events = anomaly_store.get("rule_hit_events", [])
        print(f"[LLMRouter] Loaded {len(rule_hit_events)} rule-hit events "
              f"from {anomaly_store_path}")

        os.makedirs(output_dir, exist_ok=True)
        reports: List[dict] = []

        for narrative in scrubbed_narratives:
            campaign_id = narrative.get("campaign_id", "UNKNOWN")
            confidence  = narrative.get("confidence", 0.0)

            if confidence < floor:
                print(f"[LLMRouter] Skipping {campaign_id} "
                      f"(confidence {confidence:.3f} < {floor})")
                continue

            print(f"\n[LLMRouter] Processing {campaign_id} "
                  f"(severity={narrative.get('severity')}, confidence={confidence:.3f})")

            report = self._process_one(
                narrative, rule_hit_events, mappings, anomaly_store, output_dir
            )
            reports.append(report)

        print(f"\n[LLMRouter] Done — {len(reports)} reports generated.")
        return reports

    # ------------------------------------------------------------------ #
    # Internal
    # ------------------------------------------------------------------ #

    def _process_one(
        self,
        narrative:       dict,
        rule_hit_events: List[dict],
        mappings:        Dict[str, dict],
        anomaly_store:   dict,
        output_dir:      str,
    ) -> dict:
        campaign_id   = narrative.get("campaign_id", "UNKNOWN")
        mapping_entry = mappings.get(campaign_id, {})
        ph_map        = _build_placeholder_map(narrative, mapping_entry, rule_hit_events)

        # Step 1: Build prompt with full rule evidence
        messages = build_prompt_messages(narrative, rule_hit_events, ph_map)
        prompt_chars = sum(len(m["content"]) for m in messages)
        print(f"[LLMRouter]   Prompt size: {prompt_chars:,} chars "
              f"(~{prompt_chars // 4:,} tokens)")

        # Step 2: Call the in-house Gemma (complete_json parses + retries internally)
        t_start = time.time()
        analysis, api_stats, llm_status = _call_gemma(messages)
        api_stats["wall_time_s"] = round(time.time() - t_start, 2)
        parse_method = "complete_json"
        print(f"[LLMRouter]   Gemma {llm_status} in {api_stats['wall_time_s']}s "
              f"({api_stats.get('completion_tokens', '?')} completion tokens)")

        # Step 3: Validate sections
        analysis = _validate_sections(analysis, campaign_id, config.GEMMA_MODEL)

        # Step 5: Re-identify FULL analysis dict (covers attack_timeline,
        # anomaly_explanation entities, remediation actions — everywhere
        # placeholders might be embedded)
        if mapping_entry and llm_status != "failed":
            analysis = self._scrubber.reidentify_report(analysis, mapping_entry)
            print(f"[LLMRouter]   Re-identification applied "
                  f"({len(mapping_entry.get('reverse', {}))} tokens restored)")

        # Step 6: Assemble final report
        report = self._build_final_report(
            narrative      = narrative,
            analysis       = analysis,
            rule_hit_events= rule_hit_events,
            anomaly_store  = anomaly_store,
            api_stats      = api_stats,
            parse_method   = parse_method,
            llm_status     = llm_status,
            mapping_entry  = mapping_entry,
        )

        report_path = os.path.join(
            output_dir,
            f"final_incident_report_{campaign_id}.json",
        )
        self._save_report(report, report_path)
        return report

    def _build_final_report(
        self,
        narrative:       dict,
        analysis:        dict,
        rule_hit_events: List[dict],
        anomaly_store:   dict,
        api_stats:       dict,
        parse_method:    str,
        llm_status:      str,
        mapping_entry:   dict = None,
    ) -> dict:
        """Assemble the final analyst-facing report with the new schema."""
        # Re-identify campaign context fields (entry_point may be placeholder)
        raw_ctx = {
            "campaign_id":            narrative.get("campaign_id"),
            "severity":               narrative.get("severity"),
            "confidence":             narrative.get("confidence"),
            "duration_minutes":       narrative.get("duration_minutes"),
            "first_seen":             narrative.get("first_seen"),
            "last_seen":              narrative.get("last_seen"),
            "entry_point":            narrative.get("entry_point"),
            "entity_count":           narrative.get("entity_count"),
            "kill_chain_coverage":    narrative.get("kill_chain_coverage"),
            "kill_chain_completeness":narrative.get("kill_chain_completeness"),
        }
        campaign_ctx = (
            self._scrubber.reidentify_report(raw_ctx, mapping_entry)
            if mapping_entry else raw_ctx
        )

        # IOCs from the scrubbed narrative — re-identify them
        sd = narrative.get("llm_context", {}).get("structured_data", {})
        raw_iocs = sd.get("iocs", narrative.get("iocs", {}))
        iocs = (
            self._scrubber.reidentify_report(raw_iocs, mapping_entry)
            if mapping_entry else raw_iocs
        )

        # Top-5 key evidence (already re-identified because rule_hit_events
        # were extracted from raw bundles BEFORE scrubbing)
        key_evidence = _build_key_evidence(rule_hit_events, top_n=5)

        # Per-event severity tier from rule score
        def _sev(score: float) -> str:
            if score >= 80: return "CRITICAL"
            if score >= 50: return "HIGH"
            if score >= 30: return "MEDIUM"
            return "LOW"

        # detected_anomalies — sorted by severity (CRITICAL first),
        # then by timestamp within each tier
        _SEV_ORDER = {"CRITICAL": 0, "HIGH": 1, "MEDIUM": 2, "LOW": 3}

        annotated = []
        for ev in rule_hit_events:
            score   = float(ev.get("rule_score", 0))
            severity = _sev(score)
            annotated.append({
                "severity":           severity,
                "timestamp":          ev.get("timestamp"),
                "entity":             ev.get("entity"),
                "source_ip":          ev.get("source_ip"),
                "destination_ip":     ev.get("destination_ip"),
                "destination_port":   ev.get("destination_port"),
                "event_module":       ev.get("event_module"),
                "primary_rule":       ev.get("primary_rule"),
                "all_rules_fired":    ev.get("all_rules_fired"),
                "score":              score,
                "confidence":         ev.get("rule_confidence"),
                "mitre_technique_id": ev.get("mitre_technique_id"),
                "mitre_tactic":       ev.get("mitre_tactic"),
                "kill_chain_stage":   ev.get("kill_chain_stage"),
                "what_it_means":      ev.get("what_it_means"),
            })

        detected_anomalies = sorted(
            annotated,
            key=lambda a: (_SEV_ORDER[a["severity"]], a.get("timestamp", ""))
        )

        # Severity breakdown for quick analyst scan
        anomaly_severity_breakdown = {"CRITICAL": 0, "HIGH": 0, "MEDIUM": 0, "LOW": 0}
        for a in detected_anomalies:
            anomaly_severity_breakdown[a["severity"]] += 1
        anomaly_severity_breakdown["TOTAL"] = len(detected_anomalies)

        # Detection summary stats
        store_meta    = anomaly_store.get("metadata", {})
        detection_stats = {
            "total_events_analysed":  store_meta.get("total_bundles", 0),
            "rule_hit_events":        store_meta.get("rule_hit_event_count", 0),
            "all_detection_events":   store_meta.get("all_detection_count", 0),
            "unique_rules_fired":     store_meta.get("unique_rules_fired", []),
            "rule_counts":            store_meta.get("rule_counts", {}),
            "stage_counts":           store_meta.get("stage_counts", {}),
        }

        return {
            "report_id": (
                f"RPT-{datetime.now(timezone.utc).strftime('%Y%m%d%H%M%S')}-"
                f"{narrative.get('campaign_id', 'UNKNOWN')}"
            ),
            "generated_at": datetime.now(timezone.utc).isoformat(),

            # ── Headline ──
            "executive_summary":   analysis.get("executive_summary",   {}),

            # ── Attack story ──
            "attack_timeline":     analysis.get("attack_timeline",     []),

            # ── Numerical verdict ──
            "risk_assessment":     analysis.get("risk_assessment",     {}),

            # ── Actionable fix ──
            "remediation":         analysis.get("remediation",         {}),

            # ── Top 5 plain-English signals ──
            "key_evidence":        key_evidence,

            # ── LLM analytical explanation ──
            "anomaly_explanation": analysis.get("anomaly_explanation", {}),

            # ── Severity distribution of detected anomalies ──
            "anomaly_severity_breakdown": anomaly_severity_breakdown,

            # ── Full detection record (every rule-hit event, sorted by severity) ──
            "detected_anomalies":  detected_anomalies,

            # ── Threat intel ──
            "iocs":                iocs,

            # ── Campaign metadata (non-LLM) ──
            "campaign_context":    campaign_ctx,

            # ── Detection stats ──
            "detection_stats":     detection_stats,

            # ── Processing audit trail ──
            "processing": {
                "llm_model":         config.GEMMA_MODEL,
                "api_endpoint":      config.GEMMA_BASE_URL,
                "llm_status":        llm_status,
                "json_parse_method": parse_method,
                "json_mode_used":    api_stats.get("json_mode_used", False),
                "prompt_tokens":     api_stats.get("prompt_tokens",     0),
                "completion_tokens": api_stats.get("completion_tokens", 0),
                "api_latency_ms":    api_stats.get("latency_ms",        0),
                "wall_time_s":       api_stats.get("wall_time_s",       0),
            },
        }

    @staticmethod
    def _save_report(report: dict, path: str):
        os.makedirs(os.path.dirname(path) if os.path.dirname(path) else ".", exist_ok=True)
        with open(path, "w", encoding="utf-8") as f:
            json.dump(report, f, indent=2, ensure_ascii=False)
        size_kb = os.path.getsize(path) / 1024
        print(f"[LLMRouter]   Final report → {path}  ({size_kb:.1f} KB)")
