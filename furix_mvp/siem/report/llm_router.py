def build_prompt_messages(
    narrative:        dict,
    rule_hit_events:  List[dict],
    placeholder_map:  Dict[str, str],
) -> List[dict]:
    """
    Build the system + user messages for one scrubbed narrative.
    Now includes ALL signature_rules hits grouped by rule, plus
    explicit tactic context for each rule.
    """
    sd       = narrative.get("llm_context", {}).get("structured_data", {})
    summary  = narrative.get("llm_context", {}).get("narrative_summary", "")

    campaign_id  = narrative.get("campaign_id",          sd.get("campaign_id", "UNKNOWN"))
    severity     = narrative.get("severity",             sd.get("severity", "UNKNOWN"))
    confidence   = narrative.get("confidence",           sd.get("confidence", 0.0))
    duration     = narrative.get("duration_minutes",     sd.get("duration_minutes", 0))
    first_seen   = narrative.get("first_seen",           "")[:19]
    last_seen    = narrative.get("last_seen",            "")[:19]
    completeness = narrative.get("kill_chain_completeness", 0) * 100
    n_stages     = len(narrative.get("kill_chain_coverage", []))
    entry_point  = sd.get("entry_point", narrative.get("entry_point", "unknown"))

    affected     = sd.get("affected_entities", narrative.get("affected_entities", []))
    entity_count = len(affected)
    cat_counts   = _count_by_prefix(affected)
    breakdown    = ", ".join(
        f"{count} {cat.lower().replace('_', ' ')}"
        for cat, count in sorted(cat_counts.items(), key=lambda x: -x[1])
    )

    iocs         = sd.get("iocs", narrative.get("iocs", {}))
    ext_ips      = ", ".join(iocs.get("external_ips", []))     or "none identified"
    techniques   = ", ".join(iocs.get("mitre_techniques", [])) or "see timeline"
    assets       = ", ".join(iocs.get("affected_assets", []))  or "see timeline"
    user_count   = len(iocs.get("affected_users", []))

    stages       = sd.get("attack_timeline", narrative.get("attack_stages", []))
    stages_text  = "\n".join(_format_stage(s) for s in stages) or "  (no stage data)"

    rule_text    = _format_rule_evidence_grouped(
        rule_hit_events, placeholder_map, limit_per_rule=6
    )

    # ── System prompt ──────────────────────────────────────────────
    system_prompt = (
        "You are a senior cybersecurity incident responder with expertise in "
        "threat analysis and SOC operations.\n\n"
        "You are analyzing a correlated attack campaign detected by an automated "
        "multi-layer security system. All sensitive identifiers (usernames, IP addresses, "
        "hostnames) have been replaced with typed placeholders such as EXEC_USER_1, "
        "ATTACKER_IP_2, USER_5, DBA_USER_1. Use these placeholders exactly as given "
        "in your response — do not attempt to guess or reconstruct the real names.\n\n"
        "CRITICAL — MITRE accuracy: When you reference a MITRE technique, use the "
        "tactic exactly as labelled in the prompt for that rule. Do not assign a "
        "different tactic to a technique based on general knowledge. The prompt's "
        "labels are authoritative.\n\n"
        "CRITICAL — Entity accuracy: When an event shows 'entity=<no-user-on-event>', "
        "no user account is associated with that event (firewall, network, or system "
        "event). DO NOT invent or guess a user name for these events. In attack_timeline "
        "and anomaly_explanation, describe them by the source IP placeholder or as "
        "'an internal host' / 'an external IP' — never assign a person name. The same "
        "applies if an entity field is blank: do not fabricate.\n\n"
        "Return ONLY a valid JSON object. No explanation, no markdown, no preamble. "
        "Just the JSON object matching the schema provided."
    )

    # ── User message ───────────────────────────────────────────────
    user_message = f"""## ATTACK CAMPAIGN: {campaign_id}

SEVERITY: {severity} | CONFIDENCE: {confidence:.3f} | DURATION: {duration} minutes
ATTACK WINDOW: {first_seen} → {last_seen}
KILL CHAIN: {n_stages}/14 stages covered ({completeness:.0f}% complete)
ENTRY POINT: {entry_point}
ENTITIES AFFECTED: {entity_count} total ({breakdown})

---
## PRE-ANALYSIS NARRATIVE SUMMARY
{summary}

---
## ATTACK STAGES (kill chain summary)
{stages_text}

---
## ALL RULE-HIT EVIDENCE ({len(rule_hit_events)} events grouped by rule)
{rule_text}

---
## THREAT INDICATORS (IOCs)
External IPs      : {ext_ips}
MITRE Techniques  : {techniques}
Sensitive Assets  : {assets}
User Accounts     : {user_count} individual accounts involved

---
## REQUIRED RESPONSE

Return a JSON object with EXACTLY these five top-level keys:
risk_assessment, executive_summary, attack_timeline, remediation, anomaly_explanation.

For attack_timeline: produce 6-10 timestamped narrative beats in chronological order,
using HH:MM format and the placeholder entity names. Use only stages that are
supported by evidence above. Do not invent events not present in the evidence.

For anomaly_explanation.key_indicators: cover the 5 most significant rule hits,
using the EXACT MITRE tactic labelled in the rule evidence above. Do not substitute.

{_RESPONSE_SCHEMA}"""

    return [
        {"role": "system", "content": system_prompt},
        {"role": "user",   "content": user_message},
    ]


