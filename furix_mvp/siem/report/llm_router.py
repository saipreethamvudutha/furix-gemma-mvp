def _validate_sections(
    analysis:    dict,
    campaign_id: str,
    model:       str,
) -> dict:
    stub_dict = {
        "_status": "section_unavailable",
        "_reason": "Model did not return this section in its response",
    }

    for section in REQUIRED_SECTIONS:
        if section == "attack_timeline":
            # attack_timeline should be a list, not a dict
            if section not in analysis or not isinstance(analysis[section], list):
                analysis[section] = []
                print(f"[LLMRouter] WARNING: Section '{section}' missing — empty list inserted")
        else:
            if section not in analysis or not isinstance(analysis[section], dict):
                analysis[section] = dict(stub_dict)
                print(f"[LLMRouter] WARNING: Section '{section}' missing — stub inserted")

    return analysis


# =============================================================================
#  Key evidence builder (re-identified, plain English)
# =============================================================================

def _build_key_evidence(
    rule_hit_events: List[dict],
    top_n:           int = 5,
) -> List[dict]:
    """
    Build a curated key_evidence list — top N highest-confidence rule hits
    with one-line plain English. Used AFTER re-identification so values
    in this list are real identifiers, ready for the analyst.
    """
    # Dedup by (rule, entity) so the same rule doesn't dominate
    seen: set = set()
    unique = []
    for ev in sorted(rule_hit_events,
                     key=lambda x: -float(x.get("rule_confidence", 0))):
        rule   = ev.get("primary_rule", "")
        entity = ev.get("entity", "")
        key    = (rule, entity)
        if key not in seen:
            seen.add(key)
            unique.append(ev)
        if len(unique) >= top_n * 2:   # collect 2x to allow rule diversity
            break

    result = []
    for ev in unique[:top_n]:
        result.append({
            "rule":               ev.get("primary_rule"),
            "entity":             ev.get("entity"),
            "source_ip":          ev.get("source_ip"),
            "destination_ip":     ev.get("destination_ip"),
            "what_it_means":      ev.get("what_it_means"),
            "score":              ev.get("rule_score"),
            "confidence":         ev.get("rule_confidence"),
            "mitre_technique_id": ev.get("mitre_technique_id"),
            "mitre_tactic":       ev.get("mitre_tactic"),
            "kill_chain_stage":   ev.get("kill_chain_stage"),
            "timestamp":          ev.get("timestamp"),
        })
    return result


